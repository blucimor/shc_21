#include "libft.h"

int	ft_chrcount(char *s, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (s == NULL || c == '\0')
		return (0);
	while (s[j] != '\0')
	{
		if (s[j] == c)
			i++;
		j += 1;
	}
	return (i);
}
